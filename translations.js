// ── i18n Engine ─────────────────────────────────────────────
(function() {
  var STORAGE_KEY = 'else-lang';

  function getVal(lang, path) {
    return path.split('.').reduce(function(o, k) {
      return (o && o[k] !== undefined) ? o[k] : null;
    }, ElseTranslations[lang]);
  }

  window.ElseLang = {
    current: function() {
      return localStorage.getItem(STORAGE_KEY) || 'en';
    },
    apply: function(lang) {
      if (!ElseTranslations[lang]) return;
      document.documentElement.lang = lang;
      localStorage.setItem(STORAGE_KEY, lang);

      // Text nodes
      document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var val = getVal(lang, el.dataset.i18n);
        if (val !== null) el.textContent = val;
      });

      // Placeholders
      document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
        var val = getVal(lang, el.dataset.i18nPh);
        if (val !== null) el.placeholder = val;
      });

      // Toggle button active state
      document.querySelectorAll('.lang-btn').forEach(function(btn) {
        btn.classList.toggle('active', btn.dataset.lang === lang);
      });

      // product.html re-render hook
      if (typeof window.__rerenderProduct === 'function') {
        window.__rerenderProduct(lang);
      }
      // collections.html re-render hook
      if (typeof window.__rerenderCollections === 'function') {
        window.__rerenderCollections(lang);
      }
      // index.html category grid re-render hook
      if (typeof window.__rerenderCatGrid === 'function') {
        window.__rerenderCatGrid(lang);
      }
    }
  };

  document.addEventListener('DOMContentLoaded', function() {
    var lang = ElseLang.current();
    ElseLang.apply(lang);
    document.querySelectorAll('.lang-btn').forEach(function(btn) {
      btn.addEventListener('click', function() {
        ElseLang.apply(btn.dataset.lang);
      });
    });
  });
})();
